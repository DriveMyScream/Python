class Greet:
    @staticmethod
    def Gm():
        print("Good Morning Shubham")
    
    @staticmethod
    def Ge():
        print("Good Evening Shubham")

    @staticmethod
    def Gn():
        print("Good Night Shubham")

shubham = Greet()
shubham.Gm()
shubham.Ge()
shubham.Gn()